    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String name = txtNombresDepartamento.getText().trim();
        String id = txtIdDepartamento.getText().toUpperCase();
        
        DepartamentoDto departamento = DEPARTAMENTO_DAO.read(id);
        
        
        if (name.isBlank()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Debes ingresar un nombre",
                    "Nombre vacío",
                    JOptionPane.ERROR_MESSAGE
            );
        }

        if (id.length() < 3) {
            JOptionPane.showMessageDialog(
                    this,
                    "Debes ingreser un id de tres caracteres",
                    "Id incorrecto",
                    JOptionPane.ERROR_MESSAGE
            );
        }
        
        if (departamento != null) {
            JOptionPane.showMessageDialog(
                    this,
                    "Ya existe un departamento con este id, intenta uno nuevo",
                    "Id ocupado",
                    JOptionPane.ERROR_MESSAGE
            );
        }

        if (departamento == null && !name.isBlank() && id.length() == 3) {
            nuevoDepartamento = new DepartamentoDto(id, name);
            DEPARTAMENTO_DAO.insert(nuevoDepartamento);

            JOptionPane.showMessageDialog(
                    this,
                    "Departamento ingresado correctamente",
                    "Departamento ingresado",
                    JOptionPane.INFORMATION_MESSAGE
            );
            clear();
            departamentos = DEPARTAMENTO_DAO.list();
            fillTable();
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        String name = txtNombresDepartamento.getText().trim();
        if (name.isBlank()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Debes ingresar un nombre",
                    "Nombre vacío",
                    JOptionPane.ERROR_MESSAGE
            );
        } else {
            nuevoDepartamento.setNombreDepartamento(name);
            DEPARTAMENTO_DAO.update(nuevoDepartamento);

            JOptionPane.showMessageDialog(
                    this,
                    "Departamento ingresado correctamente",
                    "Departamento ingresado",
                    JOptionPane.INFORMATION_MESSAGE
            );
            clear();
            departamentos = DEPARTAMENTO_DAO.list();
            fillTable();
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

