    private void jTableDepartamentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDepartamentosMouseClicked
        // TODO add your handling code here:
        int fila = jTableDepartamentos.rowAtPoint(evt.getPoint());
        if (fila > -1) {
            
            nuevoDepartamento = DEPARTAMENTO_DAO.read((String) datos[fila][0]);
            jefeDepartamento = EMPLEADO_DAO.getJefeDepartamento(nuevoDepartamento);
            jefeDesarrollo = EMPLEADO_DAO.getJefeProgramador(nuevoDepartamento);
            //Se llena el panel de departamento
            txtIdDepartamento.setText(nuevoDepartamento.getIdDepartamento());
            txtNombresDepartamento.setText(nuevoDepartamento.getNombreDepartamento());
            
            //
            txtJefeDepartamento.setText(
                    jefeDepartamento == null
                            ? ""
                            : jefeDepartamento.getNombreEmpleado()
            );
            txtJefeDesarrollo.setText(
                    jefeDesarrollo == null
                            ? ""
                            : jefeDesarrollo.getNombreEmpleado()
            );
            txtIdDepartamento.setEnabled(false);
            btnActualizar.setEnabled(true);
            btnIngresar.setEnabled(false);
        }


    }//GEN-LAST:event_jTableDepartamentosMouseClicked
    private void clear() {
        nuevoDepartamento = null;
        jefeDepartamento = null;
        jefeDesarrollo = null;
        txtIdDepartamento.setText("");
        txtNombresDepartamento.setText("");
        btnActualizar.setEnabled(false);
        btnIngresar.setEnabled(true);
        txtIdDepartamento.setEnabled(true);
    }

    private void fillTable() {
        //posición de la fila
        int i = 0;
        String name = txtBuscarDepartamento.getText().trim().toLowerCase();
        List<DepartamentoDto> filteredList = new LinkedList<>();
        
        for (DepartamentoDto d : departamentos) {
            if (d.getNombreDepartamento().toLowerCase().contains(name)) {
                filteredList.add(d);
            }
        }
        //Object[j][i] j=fila i=columna
        datos = new Object[filteredList.size()][COLUMNAS.length];
        for (DepartamentoDto d : filteredList) {

            EmpleadoDto jDes = null;
            EmpleadoDto jFun = null;

            for (EmpleadoDto e : empleados) {
                
                if (e.getIdDepartamento() == null) {
                    continue;
                }
                if (e.getIdDepartamento().equals(d.getIdDepartamento())) {
                    if (e.getIdRol() == RolDto.JEFE_DESARROLLO) {
                        jDes = e;
                    }
                    if (e.getIdRol() == RolDto.JEFE_FUNCIONAL) {
                        jFun = e;
                    }
                }

                if (jDes != null && jFun != null) {
                    break;
                }
            }

            datos[i][0] = d.getIdDepartamento();
            datos[i][1] = d.getNombreDepartamento();
            datos[i][2] = jDes == null ? "" : jDes.getNombreEmpleado();
            datos[i][3] = jFun == null ? "" : jFun.getNombreEmpleado();
            i++;
        }

        DefaultTableModel dtm = new DefaultTableModel(datos, COLUMNAS);
        jTableDepartamentos.setModel(dtm);
    }

