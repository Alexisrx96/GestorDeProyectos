    public VerObservaciones(CasoDto caso) {
        initComponents();
        isOpen = true;
        String[] cols = {"Porcentaje", "Fecha"};
        COLUMNAS = cols;
        title = "Observaciones del caso " + caso.getIdCaso();
        List<ObservacionCasoDto> observaciones = new ObservacionCasoDao().list(caso.getIdCaso());
        datos = new Object[observaciones.size()][cols.length];
        int i = 0;
        DateFormat df = new ConstantFormats().getDateFormat();
        for (ObservacionCasoDto b : observaciones) {
            datos[i][0] = df.format(b.getFechaObservacionCaso());
            datos[i][1] = b.getDescripcion();
            i++;
        }
        DefaultTableModel dtm = new DefaultTableModel(datos, COLUMNAS);
        jTable1.setModel(dtm);
    }

